let numeroCor = 5;
let mensagem = "";

switch (numeroCor) {
    case 1:
        mensagem = "Vermelho"
        break;
    case 2:
        mensagem = "Azul"
        break;
    case 3:
        mensagem = "Laranja"
        break;
    case 4:
        mensagem = "Amarelo"
        break;
    case 5:
        mensagem = "Verde"
        break;
    case 6:
        mensagem = "Preto"
        break;
    default:
        mensagem = "Numero de Cor Invalida!"
}

console.log(mensagem)